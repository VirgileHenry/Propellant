

/// Renders a single character.
pub struct CharacterRenderer {
    /// Character to render.
    character: char,
    /// UV of the character in the font texture atlas.
    min_uv: glam::Vec2,
    /// UV of the character in the font texture atlas.
    max_uv: glam::Vec2,
    /// what font to use in ressources
    font: u64,
    /// index of the texture of the font
    font_texture: u32,
}





